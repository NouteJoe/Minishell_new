#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		x;

	x = 0;
	while (s[x])
		x++;
	while (x >= 0)
	{
		if (s[x] == c)
			return ((char *)&s[x]);
		x--;
	}
	return (NULL);
}
