#include "libft.h"

int	ft_isalnum(int c)
{
	int	x;

	x = 0;
	if ((c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z')
		|| (c >= '0' && c <= '9'))
	{
		x = 1;
	}
	return (x);
}
